package day03;

public class C05 {
    public static void main(String[] args) {
        int num=15;
        int num2=num+2;
        System.out.println(num2);
        System.out.println(num);
        num = num+5;
        System.out.println(num);
        System.out.println(num+12);
        System.out.println(num);
        num+=5;
        System.out.println(num);
        num++;
        System.out.println(num);

        int number = 20;
        number++;//21
        System.out.println(number);//21
        System.out.println(number++);//21
        System.out.println(number);//22
        System.out.println(++number);//23
        System.out.println(number);//23
    }
}
